package comparable_and_comparator;

public class A {
	
	private int empid;
	private String name;
	private String mobile;
	private String email;
	
	public A(int empid, String name, String mobile, String email) {
		super();
		this.empid = empid;
		this.name = name;
		this.mobile = mobile;
		this.email = email;
	}

	public int getEmpid() {
		return empid;
	}

	public String getName() {
		return name;
	}

	public String getMobile() {
		return mobile;
	}

	public String getEmail() {
		return email;
	}
	
	

}
